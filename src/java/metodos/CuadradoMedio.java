package metodos;

/**
 *
 * @author elomj
 */
public class CuadradoMedio {

    private int id, xi, x2, largo, centro;

    float ri;

    public CuadradoMedio() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getXi() {
        return xi;
    }

    public void setXi(int xi) {
        this.xi = xi;
    }

    public int getX2() {
        return x2;
    }

    public void setX2(int x2) {
        this.x2 = x2;
    }

    public int getLargo() {
        return largo;
    }

    public void setLargo(int largo) {
        this.largo = largo;
    }

    public int getCentro() {
        return centro;
    }

    public void setCentro(int centro) {
        this.centro = centro;
    }

    public float getRi() {
        return ri;
    }

    public void setRi(float ri) {
        this.ri = ri;
    }

}
