package metodos;

/**
 *
 * @author elomj
 */
public class CongruencialMultiplicativo {

    private int id;

    double ri1, ri2, xi, axi, axmod;

    public CongruencialMultiplicativo() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getRi1() {
        return ri1;
    }

    public void setRi1(double ri1) {
        this.ri1 = ri1;
    }

    public double getRi2() {
        return ri2;
    }

    public void setRi2(double ri2) {
        this.ri2 = ri2;
    }

    public double getXi() {
        return xi;
    }

    public void setXi(double xi) {
        this.xi = xi;
    }

    public double getAxi() {
        return axi;
    }

    public void setAxi(double axi) {
        this.axi = axi;
    }

    public double getAxmod() {
        return axmod;
    }

    public void setAxmod(double axmod) {
        this.axmod = axmod;
    }

}
