package metodos;

/**
 *
 * @author elomj
 */
public class CongruencialLineal {

    private int id;

    double ri, axc, axcmod, xi;

    public CongruencialLineal() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getRi() {
        return ri;
    }

    public void setRi(double ri) {
        this.ri = ri;
    }

    public double getAxc() {
        return axc;
    }

    public void setAxc(double axc) {
        this.axc = axc;
    }

    public double getAxcmod() {
        return axcmod;
    }

    public void setAxcmod(double axcmod) {
        this.axcmod = axcmod;
    }

    public double getXi() {
        return xi;
    }

    public void setXi(double xi) {
        this.xi = xi;
    }

}
